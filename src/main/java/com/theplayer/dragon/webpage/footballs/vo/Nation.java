package com.theplayer.dragon.webpage.footballs.vo;

public class Nation {
	private int nation_id;
	private String nation_name;
	private boolean nation_status;
	public int getNation_id() {
		return nation_id;
	}
	public void setNation_id(int nation_id) {
		this.nation_id = nation_id;
	}
	public String getNation_name() {
		return nation_name;
	}
	public void setNation_name(String nation_name) {
		this.nation_name = nation_name;
	}
	public boolean isNation_status() {
		return nation_status;
	}
	public void setNation_status(boolean nation_status) {
		this.nation_status = nation_status;
	}
}
